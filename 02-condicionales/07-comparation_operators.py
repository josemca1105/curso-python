# Igualdad ==
print(5 == 5)  # True
print(5 == 3) # False
print("hello" == "hello") # True
print("hello" == "Hello") # False

# Desigualdad !=
print(5 != 5)  # False
print(5 != 3) # True
print("hello" != "hello") # False
print("hello" != "Hello") # True

# Mayor que >
print(7 > 5) # True
print(5 > 9) # False

# Menor que <
print(7 < 5) # False
print(5 < 9) # True

# Mayor o igual que >=
print(7 >= 5) # True
print(5 >= 9) # False
# print(5 > 5) # False
print(5 >= 5) # True

# Menor o igual que <=
print(7 <= 5) # False
print(5 <= 9) # True
# print(5 < 5) # False
print(5 <= 5) # True