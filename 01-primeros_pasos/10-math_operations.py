# Suma
print(2 + 2) # 4

# Resta
print(4 -2) # 2

# Multiplicación
print(4 * 2) # 8

# Division
print(2 / 4) # 0.5


# Otras operaciones

# Potencia
print(2 ** 3) # 8

# Division sin decimales
print(5 // 4) # 1

# Modulo (lo que sobra de la division: Residio)
print(6 % 4) # 2

# Redondear: round()
print(round(3.1)) # 3
print(round(3.9)) # 4

# Valor absoluto: abs()
print(abs(-20)) # 20